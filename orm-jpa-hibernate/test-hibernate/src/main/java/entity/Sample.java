package entity;

public class Sample {

    private long id;
    private String name;

    public Sample() {
    }

    public Sample(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
