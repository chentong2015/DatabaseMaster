    // 3. 取消JDBC代码以及设置参数和获取结果集的代码
    // 4. MyBatis特性的两种实现方式"基于XML映射"或者"Java注解"

    // 官网: https://mybatis.org/mybatis-3/zh/getting-started.html
    // APIs: https://mybatis.org/mybatis-3/es/apidocs/index.html?org/apache/ibatis
